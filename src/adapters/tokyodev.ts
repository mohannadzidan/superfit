export class TokyoDevAdapter extends PlatformAdapter {
  readonly name = "tokyodev";
  readonly icon = ICON_SVG;

  matches(url: string): boolean {
    return url.includes("tokyodev.com");
  }

  getJobTitleElement() {
    return (
      document.querySelector("#job-header h1.headline") ?? document.querySelector("#job-header h1")
    );
  }

  getCompanyElement() {
    return document.querySelector('#job-header a[href*="/companies/"]');
  }

  getJobDescriptionElement() {
    return document.querySelector<HTMLElement>("h2 + article");
  }

  getLocationElement() {
    return document.querySelector("#job-header h1.headline + div > span");
  }

  getApplyButton() {
    return document.querySelector<HTMLElement>('[data-target="modal-apply"]');
  }

  getJobId() {
    // /companies/hennge/jobs/global-internship-program-front-end
    const match = window.location.pathname.match(/\/companies\/([^/]+)\/jobs\/([^/]+)/);
    return match ? match[1] + "-" + match[2] : null;
  }

  isJobPostingPage() {
    // Check if URL pattern matches a job view or collections view
    const isJobUrl = window.location.pathname.match(/companies\/.+?\/jobs.+?/) !== null;
    return isJobUrl;
  }
}
